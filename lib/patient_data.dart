class PatientData {
  final String patientName;
  final String age;
  final String disease;
  final String hospital;
  final String date;
  final String time;

  PatientData({
    required this.patientName,
    required this.age,
    required this.disease,
    required this.hospital,
    required this.date,
    required this.time,
  });
}
